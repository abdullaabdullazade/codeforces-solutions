#include <bits/stdc++.h>
using namespace std;

void solve() {
    int n, m;
    cin >> n >> m;
    string s, t;
    cin >> s >> t;

    if (s.find('*') == string::npos) {
        if (s == t) {
            cout << "YES\n";
        } else {
            cout << "NO\n";
        }
        return;
    }

    string prefix = s.substr(0, s.find('*'));
    string suffix = s.substr(s.find('*') + 1);

    if (prefix.size() + suffix.size() > t.size()) {
        cout << "NO\n";
        return;
    }

    if (t.substr(0, prefix.size()) == prefix &&
        t.substr(t.size() - suffix.size()) == suffix) {
        cout << "YES\n";
    } else {
        cout << "NO\n";
    }
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int t = 1;
    while (t--) {
        solve();
    }

    return 0;
}


